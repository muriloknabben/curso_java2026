package main.java.com.var.app;

public class Main {
    
    public static void main(String[] args) {
        
        int y = 32; // variavel do tipo int com apelido y declarada
        double x = 10.35784; // variavel do tipo double com apelido x declarada
        System.out.println(y);
        System.out.println(x);
        System.out.printf("%.2f%n", x); // printf serve para imprimir algo formatado o %.2f serve para exibir apenas 2 casas decimais e o %n para ter uma quebra de linha
    }
}
